import React from 'react';
import { ShieldAlert, Sparkles } from 'lucide-react';
import { Badge } from '../../components/ui/Badge.jsx';
import { Tooltip } from '../../components/ui/Tooltip.jsx';
import { deriveExecutiveVerdict } from '../../lib/scoreMapping.js';

export function ExecutiveVerdict({ result }) {
  const verdict = deriveExecutiveVerdict(result);
  return (
    <section className="executive-verdict" aria-labelledby="executive-verdict-heading">
      <div>
        <div className="verdict-label-row">
          <Badge tone={result.isFallback ? 'warning' : 'cyan'}>
            {result.isFallback ? <ShieldAlert size={14} aria-hidden="true" /> : <Sparkles size={14} aria-hidden="true" />}
            {verdict.label}
          </Badge>
          {result.isFallback ? (
            <Tooltip label="Demo model result information">
              The primary AI service was unavailable, so this result was generated by BrainSNN's local demonstration model. Treat it as directional, not validated measurement.
            </Tooltip>
          ) : null}
        </div>
        <h2 id="executive-verdict-heading">{verdict.headline}</h2>
        <p>{verdict.interpretation}</p>
      </div>
      <div className="verdict-score" aria-label={`Decision score ${verdict.score} out of 100`}>
        <strong>{verdict.score}</strong>
        <span>Decision score</span>
      </div>
      <div className="verdict-grid">
        <div>
          <span>What works</span>
          <strong>{verdict.primaryStrength}</strong>
        </div>
        <div>
          <span>What hurts</span>
          <strong>{verdict.primaryRisk}</strong>
        </div>
        <div>
          <span>Best next move</span>
          <strong>{verdict.bestNextMove}</strong>
        </div>
      </div>
    </section>
  );
}
